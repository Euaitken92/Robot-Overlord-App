package com.marginallyclever.evilOverlord;

import java.awt.GridBagConstraints;

public class OverlordGridBagConstraints extends GridBagConstraints {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public OverlordGridBagConstraints() {
		this.gridx=0;
		this.gridy=0;
		this.weightx=1;
		this.weighty=0;
		this.anchor=GridBagConstraints.NORTHWEST;
		this.fill=GridBagConstraints.HORIZONTAL;
	}
}
